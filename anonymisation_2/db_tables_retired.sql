cursor get_tmp_tabs(p_src_prefix VARCHAR2,p_escape VARCHAR2) is select owner,table_name from dba_tables where owner =  p_src_prefix and table_name like '%\_TMP%' escape  p_escape;

begin
     dbms_output.put_line (' START');
 for get_tmp_tabs_rec in get_tmp_tabs(g_run_env,g_escape) loop
   begin
     execute immediate ('drop table '||get_tmp_tabs_rec.owner||'.'||get_tmp_tabs_rec.table_name);
     --dbms_output.put_line ('drop table '||get_tmp_tabs_rec.owner||'.'||get_tmp_tabs_rec.table_name);
     exception when others then
     --raise;
     null;
   end;
 end loop;
end; 
